// 扩展测试用例到 500 个
function expandTestCases(baseCases, targetCount) {
  const result = [...baseCases];
  let index = 0;

  while (result.length < targetCount) {
    const base = baseCases[index % baseCases.length];
    // 添加变体
    const variants = [
      `${base}并返回结果`,
      `${base}异常处理`,
      `${base}性能优化`,
      `${base}日志记录`,
      `${base}事务处理`,
      `${base}批量处理`,
      `${base}异步处理`,
      `${base}缓存处理`,
      `${base}并发控制`,
      `${base}错误重试`
    ];
    result.push(variants[Math.floor(Math.random() * variants.length)]);
    index++;
  }

  return result.slice(0, targetCount);
}

