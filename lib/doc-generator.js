/**
 * 生成 API 文档
 * @param {object} options - 配置选项
 */
async function generateApiDoc(options = {}) {
  const projectDir = options.projectDir || process.cwd();
  const apiDocDir = path.join(projectDir, 'docs', 'api');

  await fs.ensureDir(apiDocDir);

  const indexContent = `# API 文档

## 目录

本目录包含项目的 API 文档。

- [CLI 命令](./CLI.md) - CLI 工具使用说明
- [SDK 参考](./SDK.md) - SDK 使用参考

---

*Generated by bos-flow on ${new Date().toLocaleDateString('zh-CN')}*
`;

  const cliContent = `# CLI 命令文档

## bos-flow CLI

bos-flow 提供以下命令：

### status

查看项目状态

\`\`\`bash
node .bos-flow/bin/cli.js status
\`\`\`

### phase

管理阶段状态

\`\`\`bash
# 开始阶段
node .bos-flow/bin/cli.js phase 1 start

# 完成阶段
node .bos-flow/bin/cli.js phase 1 complete

# 查看阶段状态
node .bos-flow/bin/cli.js phase 1 status
\`\`\`

### research

研究 SDK

\`\`\`bash
node .bos-flow/bin/cli.js research "需要操作工作流数据"
\`\`\`

选项:
* k, keywords: 指定关键词
* o, output: 输出文件路径

### template

复制模板

\`\`\`bash
# 复制计划阶段模板
node .bos-flow/bin/cli.js template planning

# 复制所有模板
node .bos-flow/bin/cli.js template all
\`\`\`

选项:
* project-name: 项目名称
* author: 作者
* date: 日期

### init

初始化新项目

\`\`\`bash
node .bos-flow/bin/cli.js init <项目名称>
\`\`\`

选项:
* d, description: 项目描述
* m, mode: 工作模式 (interactive/yolo)

### doc

生成项目文档

\`\`\`bash
node .bos-flow/bin/cli.js doc readme
node .bos-flow/bin/cli.js doc changelog
\`\`\`

---

*Generated by bos-flow on ${new Date().toLocaleDateString('zh-CN')}*
`;

  await fs.writeFile(path.join(apiDocDir, 'index.md'), indexContent, 'utf8');
  await fs.writeFile(path.join(apiDocDir, 'CLI.md'), cliContent, 'utf8');

  console.log(`✓ API 文档已生成: ${apiDocDir}`);
  return apiDocDir;
}

module.exports = {
  generateReadme,
  generateChangelog,
  generateApiDoc
};
