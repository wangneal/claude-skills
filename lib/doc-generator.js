const fs = require('fs-extra');
const path = require('path');

/**
 * 生成 README.md
 * @param {object} options - 配置选项
 */
async function generateReadme(options = {}) {
  const projectDir = options.projectDir || process.cwd();
  const configPath = path.join(projectDir, '.bos-flow', 'config.json');

  // 读取项目配置
  let config = {};
  if (await fs.pathExists(configPath)) {
    config = await fs.readJson(configPath);
  }

  const readmeContent = `# ${config.name || '项目名称'}

${config.description || '项目描述'}

## 项目信息

- **项目名称:** ${config.name || '未设置'}
- **创建日期:** ${config.created_at || new Date().toLocaleDateString('zh-CN')}
- **当前阶段:** ${config.phases ? `阶段 ${config.phases.find(p => p.status === 'in_progress')?.id || 1}` : '未开始'}

## 阶段

${config.phases ? config.phases.map(p => `- ${p.id}. ${p.name} (${p.status})`).join('\n') : '- 暂无阶段信息'}

## 快速开始

\`\`\`bash
# 查看项目状态
node .bos-flow/bin/cli.js status

# 研究SDK
node .bos-flow/bin/cli.js research "需要操作工作流数据"

# 复制模板
node .bos-flow/bin/cli.js template planning
\`\`\`

## 目录结构

\`\`\`
.
├── .bos-flow/          # bos-flow 工作流目录
│   ├── bin/           # CLI 工具
│   ├── lib/           # 核心库
│   ├── sdk/           # SDK 知识库
│   ├── templates/     # 阶段模板
│   ├── config.json    # 项目配置
│   └── state.json     # 项目状态
├── .planning/          # 规划文档
│   ├── PROJECT.md     # 项目定义
│   ├── ROADMAP.md     # 项目路线图
│   ├── REQUIREMENTS.md # 需求列表
│   └── STATE.md       # 当前状态
└── README.md          # 本文件

## SDK 知识库

项目已集成金蝶 SDK 知识库，包含 ${config.sdk_classes || 0} 个类，${config.sdk_methods || 0} 个方法。

## 开发指南

### 1. 计划阶段

使用计划阶段模板进行功能分析和 SDK 调研：

\`\`\`bash
node .bos-flow/bin/cli.js template planning
\`\`\`

### 2. 开发阶段

遵循开发阶段模板进行代码实现：

\`\`\`bash
node .bos-flow/bin/cli.js template development
\`\`\`

### 3. 测试阶段

使用测试阶段模板进行全面测试：

\`\`\`bash
node .bos-flow/bin/cli.js template testing
\`\`\`

### 4. UAT 验收

使用 UAT 模板进行用户验收：

\`\`\`bash
node .bos-flow/bin/cli.js template uat
\`\`\`

---

*Generated by bos-flow on ${new Date().toLocaleDateString('zh-CN')}*
`;

  const outputPath = path.join(projectDir, 'README.md');
  await fs.writeFile(outputPath, readmeContent, 'utf8');

  console.log(`✓ README.md 已生成: ${outputPath}`);
  return outputPath;
}

